        private void txtSDT_KeyPress(object sender, KeyPressEventArgs e)
        {
            if (!char.IsDigit(e.KeyChar) && !char.IsControl(e.KeyChar))
            {
                e.Handled = true;
            }
        }

        private void btnIn_ItemClick(object sender, DevExpress.XtraBars.ItemClickEventArgs e)
        {
            SaveFileDialog saveFileDialog1 = new SaveFileDialog();
            saveFileDialog1.Filter = "File PDF|*.pdf|Excel|*.xls|Text rtf|*.rtf";
            saveFileDialog1.Title = "Xuất danh sách loại phòng";
            if (saveFileDialog1.ShowDialog() == DialogResult.OK)
            {
                if (saveFileDialog1.FilterIndex == 1)
                    gridView1.ExportToPdf(saveFileDialog1.FileName);
                if (saveFileDialog1.FilterIndex == 2)
                    gridControl1.ExportToXls(saveFileDialog1.FileName);
                if (saveFileDialog1.FilterIndex == 3)
                    gridControl1.ExportToRtf(saveFileDialog1.FileName);
            }
        }

        private void gridView1_RowCellClick(object sender, DevExpress.XtraGrid.Views.Grid.RowCellClickEventArgs e)
        {
            txtMatKhau.Text = gridView1.GetFocusedRowCellDisplayText(colMatKhau);
            txtMaNV.Text = (string)gridView1.GetFocusedRowCellValue(colMaNV);
            txtTenNV.Text = gridView1.GetFocusedRowCellDisplayText(colTenNV);
            txtSDT.Text = gridView1.GetFocusedRowCellDisplayText(colSDT);
            cbxMaCV.EditValue = gridView1.GetFocusedRowCellValue(colMaCV);
            txtDiaChi.Text = gridView1.GetFocusedRowCellDisplayText(colDiaChi);
            txtEmail.Text = gridView1.GetFocusedRowCellDisplayText(colEmail);
            dateNgaySinh.Text = gridView1.GetFocusedRowCellDisplayText(colNgaySinh);
            if ((bool)(gridView1.GetFocusedRowCellValue(colGioiTinh)))
            {
                cbxGioiTinh.Text = "Nam";
            }
            else cbxGioiTinh.Text = "Nữ";
        }
